<h4 id="size">Size</h4>

<p>width x height</p>

<ul>
	<li>Height is optional, if no height is specified the image will be a square. Example: <a href="300" class="example correct">https://dummyimage.com/300</a>
	</li>
	<li><strong>Must</strong> be the first option in the url</li>
	<li>You can specify one dimension and a ratio and dummyimage will calculate the right value. Example: <a href="/640x4:3/" class="example correct">https://dummyimage.com/640x4:3</a> or <a href="/16:9x1080/" class="example correct">https://dummyimage.com/16:9x1080</a>
	</li>
</ul>
